// FUNCTION: pwr
// PARAMS: float a, signed int b
// RETURNS: a^b where a is a float and b is an integer
// CITATION: https://www.codeproject.com/Articles/570700/SquareplusRootplusalgorithmplusforplusC
float pwr(float a, signed int b) {
    float result = 1.0;
    if (b > 0) {
        for(int i = 0; i < b; i++) {
            result *= a;
        }
    }
    if (b < 0) {
        for(int i = 0; i < (0 - b); i++){
            result *= 1 / a;
        }
    }
    return result;
}


